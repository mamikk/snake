    data = []
    for y in range(0, GRID_HEIGHT):
        col = level[y]
        for x in range(0, GRID_WIDTH):
            row = col[x]
            data.append(1 if row != ' ' else 0)

    line = '    '
    for i in range(0, len(data), 8):
        n = 0
        for j in range(i, i + 8, 1):
            n <<= 1
            if data[j]:
                n |= 1
        if i != 0:
            line += ', '
        line += '0x{:02x}'.format(n)
    if (idx + 1) < len(LEVELS):
        line += ','

    print(line)
